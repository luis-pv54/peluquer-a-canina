    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
       // Comprobando que mi tablaMascotas no este vacia primero
        if(tablaMascotas.getRowCount() >0){
            // Comprobando que se haya seleccionado a una mascota
            // -1 significa que no ha seleccionado ningun row
            if(tablaMascotas.getSelectedRow()!=-1){
                int num_cliente = Integer.parseInt(String.valueOf(tablaMascotas.getValueAt(tablaMascotas.getSelectedRow(), 0)));
                
                control.borrarMascota(num_cliente);

                
                // Ventaja emergente diciendo que se elimino correctamente 
               mensajeEmergente("Se elimino correctamente","Info","Borrado de Mascota!");
               // Cargar nuestra tabla nuevamente actualizada
               cargarTabla();
                
            }else{
                mensajeEmergente("No seleccionaste ninguna fila","Error","Error al eliminar");

            }
        
        }else{
            mensajeEmergente("Tu tabla esta vacia, no hay nada que eliminar","Error","Error al eliminar");

        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        ActualizarDatos pantallaActualizarDatos;
        
        
        if(tablaMascotas.getRowCount() >0){
            // Comprobando que se haya seleccionado a una mascota
            // -1 significa que no ha seleccionado ningun row
            if(tablaMascotas.getSelectedRow()!=-1){
                
                // obtengo id de la mascota a editar
                int num_cliente = Integer.parseInt(String.valueOf(tablaMascotas.getValueAt(tablaMascotas.getSelectedRow(), 0)));
                
                
                // Abrir la ventana para actualizar los datos
                pantallaActualizarDatos = new ActualizarDatos(num_cliente);
                pantallaActualizarDatos.setVisible(true);
                pantallaActualizarDatos.setLocationRelativeTo(null);
                
                // Para que se cierre cuando pasamos a modificar los datos
                this.dispose();
            }else{
                mensajeEmergente("No seleccionaste ninguna fila","Error","Error al eliminar");

            }
        
        }else{
            mensajeEmergente("Tu tabla esta vacia, no hay nada que eliminar","Error","Error al eliminar");

        }
        
      
    }//GEN-LAST:event_btnEditarActionPerformed
    
    private void mensajeEmergente(String mensaje, String tipo, String titulo){
        JOptionPane optionPane = new JOptionPane(mensaje);
        if(tipo.equals("Info")){
            optionPane.setMessageType(JOptionPane.INFORMATION_MESSAGE);
        }else if(tipo.equals("Error")){
            optionPane.setMessageType(JOptionPane.ERROR_MESSAGE);
        
        }
        JDialog dialog = optionPane.createDialog(titulo);
        dialog.setAlwaysOnTop(true);
        dialog.setVisible(true);
    }

