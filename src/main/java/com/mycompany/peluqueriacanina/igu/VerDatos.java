    public void cargarTabla() {
        
       
      // definir el modelo que queremos que tenga la modeloTabla
        DefaultTableModel modeloTabla = new DefaultTableModel(){
          
          //Que filas y columnas no sean editables
          @Override
          public boolean isCellEditable(int row, int column){
              return false;
          }
        };
      
      // establecemos los nombres de als columnas
      String[] titulos = {"Num","Nombre","Color","Raza","Alergico","At. Esp.", "Due√±o","Cel"};
      modeloTabla.setColumnIdentifiers(titulos);

      //carga de los datos desde la BBDD
      List<Mascota> listaMascotas = control.traerMascotas();
      
      // Recorrer la lista y mostrar cada uno de los elementos de la modeloTabla
      if(listaMascotas!=null){
          int i = 0;
          for(Mascota masco : listaMascotas){
          Object[] objeto = {masco.getNum_cliente(), masco.getNombre(), masco.getColor(), masco.getRaza(), 
              masco.getAlergico(), masco.getAtencionEspecial(), masco.getDuenio().getNombre(), 
              masco.getDuenio().getCelular()};
          
          modeloTabla.insertRow(i,objeto);
          i++;
        }
      }
      
      // Asignamos a la modeloTabla mascota la modeloTabla con nuestros datos para que se muestren
      tablaMascotas.setModel(modeloTabla);


    }
